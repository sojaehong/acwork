async function saveProductToDB() {
  loading.value = true
  try {
    const existing = new Map()

    const snap = await getDocs(collection(db, 'products'))
    snap.forEach(doc => {
      const data = doc.data()
      const key = `${data.name}-${(data.spec || '').trim()}`
      existing.set(key, { ...data, id: doc.id })
    })

    const toSave = form.items.filter(i => {
      const name = (i.name || '').trim()
      return name !== ''
    })

    if (toSave.length === 0) {
      alert('Ï†ÄÏû•Ìï† Ïú†Ìö®Ìïú Ìï≠Î™©Ïù¥ ÏóÜÏäµÎãàÎã§.')
      return
    }

    for (const i of toSave) {
      const name = i.name.trim()
      const spec = (i.spec || '').trim()
      const price = parseNumberInput(i.unit)
      const key = `${name}-${spec}`

      const existingItem = existing.get(key)

      if (existingItem) {
        if (existingItem.price !== price) {
          await setDoc(doc(db, 'products', existingItem.id), {
            name,
            spec,
            price
          })
        }
      } else {
        await addDoc(collection(db, 'products'), {
          name,
          spec,
          price
        })
      }
    }

    alert('ÌíàÎ™© DB Ï†ÄÏû• ÏôÑÎ£å')
    await loadProducts()
  } catch (e) {
    console.error('Firestore Ï†ÄÏû• Ïã§Ìå®:', e)
    alert('DB Ï†ÄÏû• Ï§ë Ïò§Î•ò Î∞úÏÉù')
  } finally {
    loading.value = false
  }
}

